          <h1>Background:</h1>
          <p>
            We chose to make a Loddon community website, since we saw an opportunity for one where school events, newsletters, news and updates, and forums could keep the Loddon community better informed and connected, and provide publicity. Since we both had some experience and interest in web programming, the idea of programming a website in our advance class as a group seemed like a good idea.
          </p>
          <h1>The project process:</h1>
             <p>
               In the first week, we named our group “Loddon Communications”, James designed the emblem which was based on what we were going to call our website, “the Loddon footprint”. The reason we named it that was for our goal statement; “To create a Loddon shire community webpage which provides news, updates and communication forums to enable individuals to “Leave their own footprint”. During the following weeks, we completed several planning stages. Each session of work, we would review a traffic light report plan [composed by James], and complete our project in stages. Parts of our planning included completing risk assessments, submitting progress reports, having meetings for the first five weeks where we would discuss all the details of our project and goal. After 4 sessions, we were ready to commence building the website. While one of us would program the functionality and general appearance of the site, the other would draw up designs, choose styles and write content. Both group members understood webpage programming in php, html, css and JavaScript, (which is what the webpage is made of) so that made it easy for both of us to develop the same thing. Ethan became the main programmer, while James drew up the website design, chose the theme of the website and wrote most of the website content.
             </p>
